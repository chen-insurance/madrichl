function post(body) {
  const buf = Buffer.from(JSON.stringify(body), "utf8");
  return new Promise((resolve, reject) => {
    const req = https.request({
      hostname: SUPABASE_HOST,
      path: "/functions/v1/manage-article",
      method: "POST",
      headers: {
        "Content-Type": "application/json; charset=utf-8",
        "apikey": ANON_KEY,
        "Authorization": `Bearer ${ANON_KEY}`,
        "x-admin-secret": ADMIN_SECRET,
        "Content-Length": buf.length,
      },
    }, (res) => {
      res.setEncoding("utf8");
      let d = "";
      res.on("data", (c) => (d += c));
      res.on("end", () => resolve({ status: res.statusCode, body: d }));
    });
    req.on("error", reject);
    req.write(buf);
    req.end();
  });
}

const ARTICLES = [lawyers, accountants, engineers, doctors, realEstate];

async function run() {
  for (const article of ARTICLES) {
    process.stdout.write(`מעדכן: "${article.title}"... `);
    const res = await post({
      action: "upsert",
      article: {
        ...article,
        is_published: true,
        published_at: new Date().toISOString(),
        author_name: "מערכת המדריך",
        is_featured: false,
      },
    });
    if (res.status < 300) {
      console.log(`✓`);
    } else {
      console.log(`✗ שגיאה ${res.status}: ${res.body.slice(0, 120)}`);
    }
  }
  console.log("\nסיום — 5 כתבות עודכנו.");
}

