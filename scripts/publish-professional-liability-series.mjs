const ARTICLES = [lawyers, accountants, engineers, doctors, realEstate];

async function run() {
  for (const article of ARTICLES) {
    process.stdout.write(`מעלה: "${article.title}"... `);
    const res = await post({
      action: "upsert",
      article: {
        ...article,
        is_published: true,
        published_at: new Date().toISOString(),
        author_name: "מערכת המדריך",
        is_featured: false,
      },
    });
    if (res.status < 300) {
      try {
        const json = JSON.parse(res.body);
        console.log(`✓ ID: ${json.id}`);
      } catch {
        console.log(`✓ (${res.status})`);
      }
    } else {
      console.log(`✗ שגיאה ${res.status}: ${res.body.slice(0, 120)}`);
    }
  }
  console.log("\nסיום — 5 כתבות פורסמו.");
}

run().catch(console.error);
